'''
# Operadores de comparação

Operador da Igualdade ==
Operador da Diferença !=
Operador maior que >
Operador menor que <
Operador maior que o igual a >=
Operador menor que o igual a <=
'''

a = int(2)
b = float(7.0)
c = int(4)
d = int(11)

print(a == b)
print(b != c)
print(a > c)
print(a < d)
print(c >= a)
print(d <= a)
